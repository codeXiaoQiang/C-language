#include <stdio.h>

int main()
{
    // 1.定义变量的格式: 数据类型 变量名称;
    // 2.为什么要指定数据类型?
    // 为了告诉操作系统 ,定义的变量对应的存储空间中, 将来能够存储什么样的数据
    // 为了告诉操作系统需要分配多大的存储空间
    // 3.为什么要指定变量名称?
    // 为了我们将来能够找到开辟好的存储空间,使用这块存储空间
    
    int intValue; // 整型变量
    float floatValue; // 实型变量 单精度
    double doubleValue; // 实型变量 双精度
    char charValue; // 字符变量
    
    // 0 bit
    // 00000000 byte(字节)
    // 1024个byte 1KB
    // 1024KB 1M
    // 1024M  1GB
    return 0;
}
